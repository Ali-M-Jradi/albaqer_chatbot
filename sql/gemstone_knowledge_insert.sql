-- General Care Guide
('Gemstone Care and Maintenance Guide',
'COMPREHENSIVE GEMSTONE CARE GUIDE

HARDNESS SCALE (Mohs) AND CARE:

VERY HARD (9-10):
Diamond, Ruby, Sapphire
- Durable for daily wear
- Can be cleaned ultrasonically
- Resistant to scratches
- Remove before impact activities

HARD (7.5-8):
Emerald, Topaz, Aquamarine, Spinel
- Good for daily wear WITH CARE
- Emeralds: NO ultrasonic cleaners
- Avoid impacts and harsh chemicals
- Clean with mild soap and water

MODERATE (6-7):
Amethyst, Citrine, Garnet, Tanzanite
- Suitable for occasional wear
- Prone to scratches from harder materials
- Store separately
- Clean gently

SOFT (2.5-5.5):
Pearl, Opal, Turquoise, Lapis Lazuli
- Very delicate, special care required
- No chemicals, perfumes, or lotions
- Wipe with soft damp cloth only
- Store separately in soft pouches

CLEANING METHODS BY STONE:

Safe for Ultrasonic Cleaning:
- Diamond (unfilled)
- Ruby (unheated okay)
- Sapphire (unheated okay)
- Amethyst, Citrine

NEVER Ultrasonic:
- Emerald (will crack)
- Pearl (will damage)
- Opal (will dry/crack)
- Tanzanite (can fracture)
- Any fracture-filled stone

Steam Cleaning - Avoid For:
- Emerald
- Opal
- Pearl
- Tanzanite
- Treated stones

STORAGE RECOMMENDATIONS:
1. Individual pouches or compartments
2. Harder stones separate from softer
3. Climate-controlled (avoid extreme temperature)
4. Low humidity for pearls and opals
5. Away from direct sunlight (some stones fade)

SETTING CARE:
- Check prongs/settings every 6 months
- Professional cleaning annually
- Re-tipping prongs as needed
- Rhodium replating for white gold

DAILY WEAR TIPS:
PUT ON LAST, TAKE OFF FIRST:
- Apply makeup, perfume, lotion first
- Then put on jewelry
- Remove jewelry before:
  * Showering/bathing
  * Swimming (chlorine damages)
  * Exercising
  * Cleaning/gardening
  * Sleeping

PROFESSIONAL MAINTENANCE:
Recommended Schedule:
- Inspection: Every 6 months
- Professional cleaning: Annually
- Re-polishing: As needed (scratches)
- Setting repair: Immediately if loose

RED FLAGS - Seek Professional Help:
- Loose stone in setting
- Damaged/bent prongs
- Scratches on stone surface
- Color change or cloudiness
- Missing stones from pave settings',
